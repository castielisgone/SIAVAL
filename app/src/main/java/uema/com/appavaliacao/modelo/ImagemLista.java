package uema.com.appavaliacao.modelo;


public class ImagemLista {

    int imagem;
    String nome;

    public ImagemLista(int imagem, String nome) {
        this.imagem = imagem;
        this.nome = nome;
    }

    public int getImagem() {
        return imagem;
    }

    public String getName() {
        return nome;
    }


}